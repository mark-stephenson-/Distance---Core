<p>
<ul>
  <li>
    Setting up any new users from your Trust to the system – this will include setting up access to the 2 user groups ie the "Healthcare unit admin” and the "Health Professional Observer" users.
  </li>
  <li>
    Setting up access for the PRASE volunteers to allow them access the app on the Ipads - NB: PRASE volunteers will not have access to the reporting system.
  </li>
  <li>
    Manage all user access for the Trust.
  </li>
  <li>
    Generate customised and standard reports (in pdf and csv formats) for own Trust.
  </li>
  <li>
    Have access to the raw values and coded datasets for all feedback collected for own Trust.
  </li>
</ul>
